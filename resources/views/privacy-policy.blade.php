# Privacy Policy for WEAPS-SSU

**Effective Date:** [Insert Date]

## 1. Introduction

Welcome to WEAPS-SSU ("we," "us," or "our"), a job placement platform designed to connect students, alumni, and employers from educational institutions. This Privacy Policy explains how we collect, use, disclose, and safeguard your information when you use our website and services.

We are committed to protecting your privacy and complying with applicable data protection laws, including the General Data Protection Regulation (GDPR) for users in the European Union and the California Consumer Privacy Act (CCPA) for users in California.

## 2. Information We Collect

### 2.1 Personal Information
We collect the following types of personal information:
- **Account Information:** Name, email address, password, phone number, and profile picture.
- **Educational Information:** School/university affiliation, degree details, graduation year, and academic records.
- **Professional Information:** Curriculum vitae (CV) details including work experience, skills, certifications, languages, projects, and social media links.
- **Application Data:** Job applications, saved jobs, and application status.
- **Verification Data:** Face detection results, AI validation results, and admin verification status.

### 2.2 Usage Data
- IP address, browser type, device information, and usage patterns.
- Log data, including system logs for security and troubleshooting.

### 2.3 Third-Party Data
- Information from social media logins (via Laravel Socialite).
- Company information provided by employers.

### 2.4 Sensitive Information
We may collect sensitive information such as facial recognition data for identity verification purposes.

## 3. How We Use Your Information

We use your information for the following purposes:
- **Service Provision:** To create and manage user accounts, process job applications, and facilitate connections between applicants and employers.
- **Verification and Security:** To verify user identities, prevent fraud, and ensure platform integrity using AI detection and admin reviews.
- **Communication:** To send notifications, updates, and respond to inquiries.
- **Improvement:** To analyze usage patterns, improve our services, and develop new features.
- **Legal Compliance:** To comply with legal obligations and enforce our terms of service.

## 4. Legal Basis for Processing (GDPR)

For users in the EU, our processing is based on:
- **Consent:** Where you have explicitly agreed to processing.
- **Contract:** Necessary for providing our services.
- **Legitimate Interest:** For security, fraud prevention, and service improvement.
- **Legal Obligation:** To comply with applicable laws.

## 5. Information Sharing and Disclosure

We do not sell your personal information. We may share your information in the following circumstances:
- **With Employers:** Job application details and CVs are shared with relevant employers for recruitment purposes.
- **Service Providers:** With trusted third-party providers for hosting, analytics, and payment processing.
- **Legal Requirements:** When required by law, court order, or to protect our rights.
- **Business Transfers:** In case of merger, acquisition, or sale of assets.
- **With Your Consent:** When you explicitly agree to sharing.

## 6. Data Storage and Retention

- **Storage Location:** Data is primarily stored on secure servers. We use cloud services compliant with international standards.
- **Retention Period:** Personal data is retained for as long as necessary for the purposes outlined, typically:
  - Account data: Until account deletion.
  - Application data: 5 years after application closure.
  - Log data: 2 years for security purposes.
- Data is deleted or anonymized when no longer needed.

## 7. Your Rights

Depending on your location, you have the following rights:

### GDPR Rights (EU Users):
- **Access:** Request a copy of your personal data.
- **Rectification:** Correct inaccurate or incomplete data.
- **Erasure:** Request deletion of your data ("right to be forgotten").
- **Restriction:** Limit processing of your data.
- **Portability:** Receive your data in a structured format.
- **Objection:** Object to processing based on legitimate interests.
- **Withdraw Consent:** Withdraw consent at any time.

### CCPA Rights (California Users):
- **Access:** Know what personal information we collect and how it's used.
- **Deletion:** Request deletion of your personal information.
- **Opt-Out:** Opt-out of the sale of personal information (though we do not sell data).
- **Non-Discrimination:** We will not discriminate against you for exercising these rights.

To exercise your rights, contact us at [privacy@weaps-ssu.com](mailto:privacy@weaps-ssu.com) or through your account settings.

## 8. Data Security

We implement robust security measures to protect your information:
- Encryption of data in transit and at rest.
- Access controls and regular security audits.
- AI-powered fraud detection and monitoring.
- Regular backups and incident response plans.

Despite these measures, no system is completely secure. We encourage you to use strong passwords and report any suspicious activity.

## 9. Cookies and Tracking

